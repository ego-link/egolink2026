from typing import List, Dict, Optional, Any, Tuple
import json
import requests

# ===================== Benchmark Server URL (Provided uniformly during the competition) =====================
BENCHMARK_SERVER = "http://比赛服务器地址/api/agent/interact" 
TIMEOUT = 30


class AgentBenchmark:
    def __init__(self, server_url: str = BENCHMARK_SERVER):
        self.server_url = server_url
        self.session = requests.Session()

    def step(self, candidate_id: str, scenario: str, scenario_id: str, use_tools: bool, messages: List[Dict[str, str]]) -> Dict[str, Any]:
        """
        [Core Interface] The agent calls this method for a single round of interaction.
        :param candidate_id: Competitor ID
        :param scenario_id: Current scenario ID
        :param task_id: Specific task ID under the current scenario
        :param use_tools: If True, it means the latest interaction content is a tool call JSON list; if False, it means the latest interaction content is a response string to the user. Choose only one.
        :param messages: The list of historical interaction rounds for the current episode (competitors need to maintain this themselves), ultimately ending with the "assistant" role. Format:
            [
                {"role": "assistant", "content": "Output sent by the agent to the server for the user in the first round"}, 
                {"role": "user", "content": "User reply returned by the server in the first round"},
                {"role": "assistant", "content": [{"JSON list of tool calls sent by the agent to the server in the first round"}]}
                {"role": "tools", "content": "Tool execution results returned by the server in the first round"}  # Use this role for tool return results
                ...
                ... The above are all historical interaction records of the current episode
                {"role": "assistant", "content": "The interaction information generated by the agent in the latest round to be sent to the server"}
            ]
            Note: Each time, the assistant's content can only be either a tool_call or a response_to_user. Place the tool_call in a "[]" list conforming to JSON format; place the response_to_user in a "string".

        
        :return: The benchmark server returns { "stop": bool, "role": "user/tools", "content": str }
        """
        # Fix: Use correct parameter names, completely pass all fields required by the interface
        payload = {
            "candidate_id": candidate_id,
            "scenario_id": scenario_id,
            "task_id": task_id,
            "use_tools": use_tools,
            "messages": messages
        }

        try:
            resp = self.session.post(
                self.server_url,
                json=payload,
                timeout=TIMEOUT,
                headers={"Content-Type": "application/json"}
            )
            resp.raise_for_status()
            return resp.json()

        except Exception as e:
            return {
                "role": "error",
                # If a network error occurs, forcibly add the STOP character to prevent infinite loops
                "content": f"Interface call failed: STOP {str(e)}" 
            }

    @staticmethod
    def is_terminated(content: str) -> Tuple[bool, bool]:
        """Determine whether to end this round of dialogue by matching STOP and SUCCESS keywords in the text"""
        if not isinstance(content, str):
            return False, False

        is_stop = "STOP" in content
        is_success = "SUCCESS" in content if is_stop else False
        
        return is_stop, is_success


